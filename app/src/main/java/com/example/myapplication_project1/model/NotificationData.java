package com.example.myapplication_project1.model;

public class NotificationData {
    String no,upda;

    public NotificationData(String no, String upda) {
        this.no = no;
        this.upda = upda;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getUpda() {
        return upda;
    }

    public void setUpda(String upda) {
        this.upda = upda;
    }
}
